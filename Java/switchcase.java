public class switchcase {
    public static void main(String args[])
    {
        int n=6;

        switch(n)
        {
            case 1:
            System.out.println("one");
            break;

            case 2:
            System.out.println("two");
            break;

            case 3:
            System.out.println("three");
            break;
           
            default:
            System.out.println("Invalid");

        }
    }
}
